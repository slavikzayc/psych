#pragma once

#include <string>

#include "ecs/Registry.h"
#include "game/GameState.h"

struct WorldState {
  // ECS-хранилище всех живых сущностей и их компонентов.
  // В нём лежат игрок, пациенты, предметы, двери и интерактивные объекты.
  Registry registry;

  // Текущий режим игры определяет, какой экран рисуется и какая логика ввода
  // работает.
  GameMode mode = GameMode::MainMenu;

  // Запоминает, куда вернуться после закрытия инвентаря.
  // Например: из диалога открыли инвентарь, значит вернуться нужно в
  // DialogueCombat.
  GameMode inventory_return_mode = GameMode::Exploration;

  // Текущая фаза смены: ранняя, средняя или финальная.
  NightPhase phase = NightPhase::Early;

  // Идентификатор текущей карты. Сущности фильтруются по MapComponent::map_id.
  int currentmap_id = 1;

  // Сколько игровых секунд прошло с начала смены.
  int shift_time_seconds = 0;

  // Длительность смены в игровых секундах. При достижении этого значения
  // наступает Victory.
  int shift_duration_seconds = 900;

  // Entity игрока. Нужен системам как быстрый доступ к компонентам игрока.
  Entity player = 0;

  // Entity пациента, с которым сейчас идёт диалог.
  // Если диалога нет, значение равно 0.
  Entity active_patient = 0;

  // Главный флаг работы приложения. Пока true, Game::Run продолжает главный
  // цикл.
  bool running = true;

  // Задел под отладочные режимы. Сейчас почти не используется, но оставлен как
  // поле состояния.
  bool dev_mode = false;

  // Последнее игровое сообщение для HUD и экранов.
  // Его меняют системы движения, взаимодействия, инвентаря, диалога и ночных
  // событий.
  std::string message;
};
