#pragma once

enum class GameMode {
  // Главное меню. В этом режиме игра не обновляет время смены и рассудок.
  MainMenu,

  // Экран распределения характеристик перед началом смены.
  StatAllocation,

  // Основной режим: игрок ходит по карте, взаимодействует с объектами,
  // подбирает предметы и начинает диалоги.
  Exploration,

  // Экран инвентаря. Он может быть открыт как из исследования, так и из
  // диалога.
  Inventory,

  // Режим диалогового боя с пациентом.
  DialogueCombat,

  // Победа: смена дошла до конца, рассудок игрока выше нуля.
  Victory,

  // Поражение: рассудок игрока упал до нуля.
  Defeat
};

// Фаза ночи вычисляется по прогрессу таймера смены.
// Она используется ночными событиями и HUD.
enum class NightPhase { Early, Middle, Final };
